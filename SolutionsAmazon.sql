CREATE VIEW TopRatedContent AS
SELECT content_id,title,type,imdb_rating
FROM Content
WHERE imdb_rating >= 8.0
ORDER BY imdb_rating DESC;

DELIMITER $$
CREATE PROCEDURE GetContentByGenre(IN genreName VARCHAR(100))
BEGIN
    SELECT content_id,title,type,genre,imdb_rating
    FROM Content
    WHERE genre LIKE CONCAT('%',genreName,'%');
END $$
DELIMITER ;

DELIMITER $$
CREATE TRIGGER after_rating_insert
AFTER INSERT ON Ratings
FOR EACH ROW
BEGIN
    INSERT INTO WatchHistory (watch_id,user_id,content_id,watched_on,progress_percent)
    VALUES ( (SELECT IFNULL(MAX(watch_id),0)+1 FROM WatchHistory), NEW.user_id, NEW.content_id, NEW.rating_date, 100);
END $$
DELIMITER ;

